n = input ('Digite algo: ')
print ('Tipo primitivo: {}'.format(type(n)))
print('É somente espaços? {}'.format(n.isspace()))
print('É um número? {}'.format(n.isnumeric()))
print('É afabético? {}'.format(n.isalpha()))
print('É alfanúmerioco? {}'.format(n.isalnum()))
print('Está em maiúscula? {}'.format(n.isupper()))
print('Está em minúscula? {}'.format(n.islower()))




